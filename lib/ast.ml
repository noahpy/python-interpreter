
(* Unapplied function: An expression with variables needed to be filled for evaluation. 
   Should be generated by the parser. Going back from the relevant statement(s), it 
   reduces the whole function into a single expression. Then, before evaluating this
   expression, it overwrites the state according to local definitions and the given
   argument values. Then, it evaluates the expression and saves the result to the program 
   state. Then, it destructs all local assignments before exiting. *)
type func_unapp = value list -> program_state -> value


(* Abstract syntax tree of python subset *)

and value =
    | IntV of int
    | FloatV of float
    | StringV of string
    | BoolV of bool
    | Ntwo
    | Exception of string
    | Function of func_unapp

and bin_op = 
    | Add
    | Mul
    | Sub
    | Div
    | And
    | Or
    | Less
    | Greater
    | Leq
    | Geq
    | Equal
    | Neq

and expr = 
    | Value of value
    | Bin_Exp of (expr * bin_op * expr)
    | Var_Ref of string
    | Func_App of (string * expr list)

and statement = 
    | Expr of expr
    | Assign of (string * expr)
    | Func_Def of (string * expr)

and program = statement list

(* Program state: Used to keep track of variables (including functions) and current position. *)
and program_state = {
    program: program;
    ip: int;
    variables: (string, expr) Hashtbl.t;
}

(* Helper functions *)

let value_to_output val_x =
    match val_x with
      | IntV x -> print_int x; print_newline();
      | FloatV x -> print_float x; print_newline();
      | StringV x -> print_string x; print_newline();
      | BoolV x -> print_string (if x then "True" else "False"); print_newline();
      | Exception x -> print_string x; print_newline();
      | Ntwo -> print_string "None"; print_newline();
      | Function f -> print_string "Function"; print_newline();
